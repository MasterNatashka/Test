'''Разработать программу с параллельной разработкой алгоритма, выполняющую задачу:
1. первая функция, формирование массива [1, j ,1000];
2. вторая функция математическая задчка на выбор, например число Фибоначчи, множество Мандельброта'''

# Импорт библиотек
from multiprocessing import Process
import time

# Объявление переменной и фиксирование начального значения отсчёта времени
t0 = time.time_ns()

# Объявление формирования списка чисел
my_list = []

# Функция формирования массива от 1 до 1000
def mass():
    global my_list
    for i in range(1, 1001):
        my_list.append(i)
    return my_list

# Функция вычисления n-го числа Фибоначчи
def fibonacci(n):
    m, p = 0, 1
    for i in range(n):
        m, p = p, m + p
    print(f"[Процесс 1] Длина получившегося числа Фибоначчи (в знаках): {len(str(p))} | Время выполнения: {(time.time_ns()-t0)/ 10**9} секунд")
    return p

def heavy_computation_1():
    #Первый процесс: вычисляет 5000-е число Фибоначчи
    print("[Процесс 1] Начинаю вычисление 5000-го числа Фибоначчи...")
    result = fibonacci(5000)
    #print(f"[Процесс 1] 5000-е число Фибоначчи = {result}")  

def heavy_computation_2():
    #Второй процесс: формирование массива [1, j ,1000]
    print("[Процесс 2] Формирую массив чисел от 1 до 1000")
    result = mass()
    #print(f"[Процесс 2] Массив чисел = {result}")
    print(f"[Процесс 2] Размер сформированного списка: {len(my_list)} | Время выполнения: {(time.time_ns()-t0)/ 10**9} секунд")

if __name__ == '__main__':
    print("Запускаем два процесса для параллельных вычислений")

    # Создаем процессы
    p1 = Process(target=heavy_computation_1)
    p2 = Process(target=heavy_computation_2)

    # Запускаем оба процесса (выполняются параллельно)
    p1.start()
    p2.start()

    # Ждем завершения
    p1.join()
    p2.join()

    print(f'Время выполнения глобального процесса {(time.time_ns()-t0)/ 10**9} секунд')
    p2.join()

    print("\nОба процесса завершены!")